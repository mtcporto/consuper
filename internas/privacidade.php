                                <strong>6.2 – DAS DISPOSIÇÕES ESPECÍFICAS</strong><br>

                                                <strong>6.2.1 – DO SIGNIFICADO</strong><br>
               6.2.2 Cookies são pequenos arquivos que coletam informações. Pela ação dos cookies é possível que um website consiga registrar e “recordar” ações de cada usuário que o visita.
                <br><br>
6.2.3 Os Cookies possibilitam não apenas reconhecer automaticamente o dispositivo em uma próxima visita, como também monitorar o padrão de busca dos usuários, para que a oferta dos produtos e serviços seja cada vez mais personalizada e acertada.
