6.2.5 O usuário possui a liberalidade de, a qualquer tempo, acessar nosso gerenciador de cookies (CookiePro, da OneTrust, disponível em nossas páginas) para aceitar, recusar, requerer notificação ou até mesmo bloquear a entrada de cookies no sistema.
                <br><br>
6.2.6 A não aceitação dos cookies pode vir a limitar algumas funcionalidades do site.                
            </p>
                <strong>6.3 – DA JUSTIFICATIVA</strong><br>
            <p style="margin-left: 22px; text-align: justify;">
6.3.1 Os cookies são usados para melhorar a sua experiência do usuário no website. Afinal, eles armazenam as preferências dos visitantes, e assim, facilitam suas navegações, tornando-as mais rápidas e eficientes.
                <br><br>
6.3.2 De igual forma, os Cookies são utilizados com a finalidade de compilar estatísticas anônimas e agregadas que ajudam a compreender como as pessoas utilizam a plataforma digital.
                <br><br>
  6.3.3 Devem ser observadas as Políticas de Privacidade de parceiros como servidores e provedores de internet que podem eventualmente armazenar os dados coletados no ato de inscrição dos participantes do evento – para informações relativas à utilização de Cookies por eles.     
                  <br><br>
6.3.4 Cookies de publicidade: anúncios tais como “web beacons”, “pixels” e “tags” de redes de anúncios, que ajudam a oferecer propagandas relevantes de forma mais eficaz.
                  <br><br>
6.3.5 Estes Cookies servem para direcionar a publicidade em função dos interesses de cada usuário e do número de visitas que realizaram, permitindo limitar a exibição do anúncio a um certo número de vezes.
                  <br><br>
6.3.6 Boa parte deste tipo de Cookies monitoram os usuários através do ID ou endereço de IP do seu dispositivo, e assim, conseguem coletar alguns dados pessoais.
                  <br><br>
6.3.7 Ademais, estes Cookies permitem que, por meio de anúncios segmentados, a eficácia do website da CONSUPER seja avaliada.
                  <br><br>
6.3.8 Cookies essenciais: esses cookies permitem a navegação completa no website da CONSUPER.
                  <br><br>
6.3.9 Caso não ocorra a autorização destes Cookies, alguns serviços do website ficarão limitados
                  <br><br>
6.3.10 Cookies analíticos: esses cookies são utilizados para que, analisando e monitorando o acesso ao website, um padrão de comportamento dos usuários possa ser definido. Eles têm o exclusivo efeito de criação e análise estatística da empresa.
                  <br><br>
6.3.11 Cookies da Sessão: esses Cookies são temporários. Eles são apagados no momento em que acaba a visita ao website e o navegador é fechado.
                  <br><br>
6.3.12 Cookies Persistentes: são os que mantêm as informações registradas por um período de tempo. Eles continuam no desktop ou dispositivo móvel mesmo depois que o usuário fechar o navegador ou efetuar uma reinicialização.  À título de exemplo pode-se mencionar um cadastro feito para efetivação de compras no website.
                  <br><br>
6.3.13 Cookies de funcionalidade: são aqueles que guardam as preferências do usuário no que diz respeito à utilização do website, de forma que não seja necessário voltar a configurar o site a cada visitação.                  
            </p>
